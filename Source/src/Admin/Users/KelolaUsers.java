    private void tampilkanDataKeTabel()
    {
        try
        {
            String query = "SELECT " +
                            "u.ID_User, " +
                            "p.Nama AS Nama_Pegawai, " +
                            "u.Nama_User, " +
                            "u.Password_Hash, " +
                            "u.Is_Admin " +
                            "FROM users u " +
                            "JOIN pegawai p ON u.ID_Pegawai = p.ID_Pegawai";

            ResultSet result = DBConnection.getData(query);

            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            model.setRowCount(0);

            while (result.next())
            {
                model.addRow(new Object[] {
                    result.getString("ID_User"),
                    result.getString("Nama_User"),
                    result.getString("Nama_Pegawai"),
                    result.getBoolean("Is_Admin") ? "Admin" : "User",
                    result.getString("Password_Hash"),
                });
            }
        } catch (SQLException e)
        {
            MessageDialog.showProgramErrorMessage(e.getMessage());
        }
    }
    
