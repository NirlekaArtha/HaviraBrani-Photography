    private void pilihItemDenganAkhiran(JComboBox<String> comboBox, String target) {
        for (int i = 0; i < comboBox.getItemCount(); i++) {
            String item = comboBox.getItemAt(i);
            if (!item.isEmpty() && item.endsWith(target)) {
                comboBox.setSelectedItem(item);
                break;
            }
        }
    }
    
    private void loadPilihanComboBox()
    {
        ResultSet dataPegawai = DBConnection.getData("SELECT ID_Pegawai, Nama FROM pegawai");
        
        try
        {
            while(dataPegawai.next())
            {
                inputPegawai.addItem(dataPegawai.getString("ID_Pegawai") + " - " + dataPegawai.getString("Nama"));
            }
            
        }catch(SQLException e)
        {
            MessageDialog.showProgramErrorMessage(e.getMessage());
        }
    }
    
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String username = inputUsername.getText();
        String password = inputPassword.getText();
        String ID_Pegawai = inputPegawai.getSelectedItem().toString().substring(0,1);
        int role = inputRole.getSelectedIndex();

        if (username.isEmpty() ||
            ID_Pegawai.isEmpty() ||
            password.isEmpty())
        {
            MessageDialog.showUserErrorMessage("Semua field harus diisi!");
            return;
        }
        
        String hashedPassword = HashUtil.hashPassword(password);
        
        String query = "INSERT INTO users (Nama_User, ID_Pegawai, Password_Hash, Is_Admin) VALUES"
                        + "(?, ?, ?, ?)";
        Object[] parameters = {username, ID_Pegawai, hashedPassword, role};
        int result = DBConnection.updateData(query, parameters);
        
        if (result < 1)
        {
            return;
        }else 
        {
            MessageDialog.showSuccessMessage("Data berhasil ditambah, data yang ditambah: " + result);            
        }
        
        tampilkanDataKeTabel();
        inputUsername.setText("");
        inputPegawai.setSelectedIndex(0);
        inputRole.setSelectedIndex(0);
        inputPassword.setText("");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        
        int selectedRow = tabel.getSelectedRow();
        this.ID_User = String.valueOf(tabel.getValueAt(selectedRow, 0));
        
        String query = "SELECT " +
                        "u.ID_User, " +
                        "p.Nama AS Nama_Pegawai, " +
                        "u.Nama_User, " +
                        "u.Password_Hash, " +
                        "u.Is_Admin " +
                        "FROM users u " +
                        "JOIN pegawai p ON u.ID_Pegawai = p.ID_Pegawai " +
                        "WHERE u.ID_User = ?";

        Object[] parameters = {ID_User};
        ResultSet result = DBConnection.getData(query, parameters);
        
        try
        {
            if (result.next())
            {
                inputUsername.setText(result.getString("Nama_User"));
                pilihItemDenganAkhiran(inputPegawai, result.getString("Nama_Pegawai"));
                inputRole.setSelectedIndex(result.getInt("Is_Admin"));
                System.out.print(result.getInt("Is_Admin"));
                inputPassword.setText("");
            }
        }catch (SQLException e)
        {
            MessageDialog.showProgramErrorMessage(e.getMessage());
        }
        
    }//GEN-LAST:event_tabelMouseClicked

    private void btnPerbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerbaruiActionPerformed
        String username = inputUsername.getText();
        String password = inputPassword.getText();
        String ID_Pegawai = inputPegawai.getSelectedItem().toString().substring(0,1);
        int role = inputRole.getSelectedIndex();
        
        if (username.isEmpty() ||
            ID_Pegawai.isEmpty() ||
            password.isEmpty() )
        {
            MessageDialog.showUserErrorMessage("Semua field harus diisi!");
            return;
        } else if (this.ID_User.isEmpty())
        {
            MessageDialog.showUserErrorMessage("Silahkan pilih baris pada tabel terlebih dahulu");
        }
        
        String hashedPassword = HashUtil.hashPassword(password);
        
        String query = "UPDATE users SET Nama_User = ? , ID_Pegawai = ? , Password_Hash = ?, Is_Admin = ?"
                        + "WHERE ID_User = ?";
        Object[] parameters = {username, ID_Pegawai, hashedPassword, role,this.ID_User};
        int result = DBConnection.updateData(query, parameters);
        
        if (result < 1)
        {
            return;
        }else 
        {
            MessageDialog.showSuccessMessage("Data berhasil diperbarui, data yang diperbarui: " + result);            
        }
        
        tabel.clearSelection();
        this.ID_User = "";
        tampilkanDataKeTabel();
        inputUsername.setText("");
        inputPegawai.setSelectedIndex(-1);
        inputPassword.setText("");
        inputRole.setSelectedIndex(-1);
    }//GEN-LAST:event_btnPerbaruiActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (MessageDialog.showConfirmDialog("Apakah anda yakin ingin menghapus data dengan ID: " + this.ID_User + " ?"))
        {
            String query = "DELETE FROM users WHERE ID_User = ?";
            int result = DBConnection.updateData(query, new Object[] {this.ID_User});
            
            if (result >= 1)
            {
                MessageDialog.showSuccessMessage("Data berhasil dihapus, banyaknya data yang dihapus: " + result);
            }
            
            tabel.clearSelection();
            this.ID_User = "";
            tampilkanDataKeTabel();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

